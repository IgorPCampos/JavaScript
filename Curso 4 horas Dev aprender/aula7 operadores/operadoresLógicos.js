// Operador Lógicos e (&&)
// retorna true se os dois operandos forem true, se um não for vai ser false
console.log("Operador lógico e(&&)")
console.log(true && true)
console.log(false && true)
console.log(true && false)
console.log(false && false)
// Operador Lógicos ou (||)
// retorna true se um dos dois operandos forem true, se os dois forem false retorna false 
console.log("Operador lógico ou(||)")
console.log(true || true)
console.log(false || true)
console.log(true || false)
console.log(false || false)

//Operador não (!)
// != (diferente)
let num = 1
console.log("Operador lógico não(!)")
console.log(num!=1)
console.log(num!=2)